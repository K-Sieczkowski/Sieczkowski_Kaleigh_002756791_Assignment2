    private void viewDocBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDocBtnActionPerformed
        int selectedRowIndex = docTbl.getSelectedRow();

        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to view.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) docTbl.getModel();
        Doctor selectedDoc = (Doctor) model.getValueAt(selectedRowIndex, 0);

        docSpcTxt.setText(String.valueOf(selectedDoc.getJobTitle()));
        docNameTxt.setText(String.valueOf(selectedDoc.getPerson().getName()));
        docHospitalTxt.setText(String.valueOf(selectedDoc.getHospital().getName()));
        docPhoneTxt.setText(String.valueOf(selectedDoc.getPerson().getPhoneNumber()));
        docEmailTxt.setText(String.valueOf(selectedDoc.getPerson().getEmailAddress()));
        docCityTxt.setText(String.valueOf(selectedDoc.getHospital().getCity().getName()));
        docHospitalZipTxt.setText(String.valueOf(selectedDoc.getHospital().getCity().getZipcode()));
    }//GEN-LAST:event_viewDocBtnActionPerformed

