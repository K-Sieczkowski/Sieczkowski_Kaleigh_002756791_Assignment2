    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        int selectedRowIndex = hospitalTbl.getSelectedRow();

        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to view.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) hospitalTbl.getModel();
        Hospital selectedHospital = (Hospital) model.getValueAt(selectedRowIndex, 0);

        hospitalTxt.setText(String.valueOf(selectedHospital.getName()));
        cityTxt.setText(String.valueOf(selectedHospital.getCity().getName()));
        zipCodeTxt.setText(String.valueOf(selectedHospital.getCity().getZipcode()));
        addressTxt.setText(String.valueOf(selectedHospital.getHouse().getAddress()));
    }//GEN-LAST:event_viewBtnActionPerformed

