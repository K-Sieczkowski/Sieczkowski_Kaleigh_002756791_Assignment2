    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) hospitalTbl.getModel();
        model.setRowCount(0);
        
        for(Hospital h : hospitals.getHospitals()){
            Object[] row = new Object[4];
            row[0] = h;
            row[1] = h.getCity().getName();
            row[2] = h.getCity().getZipcode();
            row[3] = h.getHouse().getAddress();
            
            model.addRow(row);
        }
       
    }
    
    
    private void populateDocTable(){
        DefaultTableModel model2 = (DefaultTableModel) docTbl.getModel();
        model2.setRowCount(0);
        for(Doctor d : doctors.getDoctorList()){
            Object[] row = new Object[7];
            row[0] = d;
            row[1] = d.getPerson().getName();
            row[2] = d.getHospital().getName();
            row[3] = d.getHospital().getCity().getName();
            row[4] = d.getHospital().getCity().getZipcode();
            row[5] = d.getPerson().getPhoneNumber();
            row[6] = d.getPerson().getEmailAddress();
            
            model2.addRow(row);
        }
        
    }
    
    
    public void newFilter(String s){
        DefaultTableModel model = (DefaultTableModel) hospitalTbl.getModel();
        TableRowSorter<DefaultTableModel> t = new TableRowSorter<DefaultTableModel>(model);
        hospitalTbl.setRowSorter(t);
        t.setRowFilter(RowFilter.regexFilter(s));
        
    }
    
    public void newDocFilter(String s){
        DefaultTableModel model = (DefaultTableModel) docTbl.getModel();
        TableRowSorter<DefaultTableModel> t = new TableRowSorter<DefaultTableModel>(model);
        docTbl.setRowSorter(t);
        t.setRowFilter(RowFilter.regexFilter(s));
        
    }
    
